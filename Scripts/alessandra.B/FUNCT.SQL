CREATE OR REPLACE PROCEDURE UPDATE_CODER_SALARY(
  P_CODER_ID IN CODERS.CODER_ID%TYPE,
  P_FACTOR IN NUMBER
  ) AS 
     V_COUNT INTEGER;
     
BEGIN
   
   SELECT COUNT(ROWID)
   INTO V_COUNT
   FROM CODERS
   WHERE CODER_ID = P_CODER_ID;
   
     IF V_COUNT = 1 THEN
      UPDATE CODERS
      SET SALARY = SALARY + (SALARY * P_FACTOR)
      WHERE CODER_ID = P_CODER_ID;
      COMMIT;
   
     END IF;
     
   EXCEPTION 
   WHEN OTHERS THEN 
       ROLLBACK;
   
END UPDATE_CODER_SALARY;
/

