--PROCEDURE INCREASE SALARY_CODER_107
CREATE OR REPLACE PROCEDURE INCREASED_SALARY_CODER_107
AS 
    
BEGIN
UPDATE CODERS
SET SALARY = SALARY + 1
WHERE CODER_ID = 107;
COMMIT;

END;
/

CALL INCREASED_SALARY_CODER_107();
BEGIN
 increased_salary_coder_107();
 
 END;
 
 
CREATE OR REPLACE PROCEDURE INCREASED_SALARY_CODER(
 P_CODER_ID IN CODERS.CODER_ID%TYPE
)
AS 
    
BEGIN
UPDATE CODERS
SET SALARY = SALARY + 1
WHERE CODER_ID = P_CODER_ID;
COMMIT;

END;
/
